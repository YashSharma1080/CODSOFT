while True :
    num1 = float(input("Enter any number:"))
    num2 = float(input("Enter another number:"))
    O = input("Enter the operation to perform: ")
    if O == "+" :
     print("sum =",num1 + num2)
    if O == "-" :
     print("diff =",num1 - num2)
    if O == "*" :
     print("product =",num1 * num2)
    if O == "/" :
     print("quotient =",num1 / num2)
    if O == "**" :
     print("power =",num1 ** num2)
    
        
